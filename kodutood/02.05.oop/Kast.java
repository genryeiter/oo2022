public class Kast {
    private String kastityyp;
    private double kastihind;
    private double kastimass;
    private double pesadeArv;

    public String getKastityyp() {
        return kastityyp;
    }

    public void setKastityyp(String kastityyp) {
        this.kastityyp = kastityyp;
    }

    public double getKastihind() {
        return kastihind;
    }

    public void setKastihind(double kastihind) {
        this.kastihind = kastihind;
    }

    public double getKastimass() {
        return kastimass;
    }

    public void setKastimass(double kastimass) {
        this.kastimass = kastimass;
    }

    public double getPesadeArv() {
        return pesadeArv;
    }

    public void setPesadeArv(double pesadeArv) {
        this.pesadeArv = pesadeArv;
    }

}
